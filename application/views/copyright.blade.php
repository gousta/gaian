<h2>
              What is Copyright?
            </h2>
<h3>
              Which types of work are subject to copyright?
            </h3>

            <p>
              When a person creates an original work that is fixed in a physical medium, he or she
              automatically owns copyright to the work. Copyright ownership gives the owner the
              exclusive right to use the work in certain, specific ways. Many types of works are
              eligible for copyright protection, including:
            </p>
            <ol>
              <li>
                <strong>Audiovisual works</strong>, such as TV shows, movies, and online videos
              </li>
              <li>
                <strong>Sound recordings and musical compositions</strong>
              </li>
              <li>
                <strong>Written works</strong>, such as lectures, articles, books, and musical
                compositions
              </li>
              <li>
                <strong>Visual works</strong>, such as paintings, posters, and advertisements
              </li>
              <li>
                <strong>Video games and computer software</strong>
              </li>
              <li>
                <strong>Dramatic works</strong>, such as plays and musicals
              </li>
            </ol>
           <p>
              Ideas, facts, and processes are not subject to copyright. In order to be eligible for
              copyright protection, a work must be both <strong>creative</strong> and
              <strong>fixed</strong> in a tangible medium. Names and titles are not, by themselves,
              subject to copyright protection.
            </p>
            <h3>
              Is it possible to use a copyright-protected work without infringing?
            </h3>
            <p>
              In some circumstances, it is possible to use a copyright-protected work without
              infringing the owner’s copyright. 
            </p>
            <ol>
              <li>
                <strong>Given credit</strong> to the copyright owner
              </li>
              <li>
                <strong>Refrained from monetizing</strong> the infringing video
              </li>
              <li>
                <strong>Noticed similar videos</strong> that appear on YouTube
              </li>
              <li>
                <strong>Purchased the content</strong> on iTunes, a CD, or DVD
              </li>
              <li>
                <strong>Recorded the content yourself</strong> from TV, a movie theater, or the
                radio
              </li>
              <li>
                <strong>Stated that “no copyright infringement is intended”</strong>
              </li>
            </ol>
            










